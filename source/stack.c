#include <stdlib.h>

#include "mac/list.h"
#include "mac/stack.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ mac_stack_push ------------------------------  *
*                                                                            *
*****************************************************************************/

int mac_stack_push(Stack *stack, const void *data) {

/*****************************************************************************
*                                                                            *
*  Push the data onto the stack.                                             *
*                                                                            *
*****************************************************************************/

return mac_list_ins_next(stack, NULL, data);

}

/*****************************************************************************
*                                                                            *
*  ------------------------------ mac_stack_pop -------------------------------  *
*                                                                            *
*****************************************************************************/

int mac_stack_pop(Stack *stack, void **data) {

/*****************************************************************************
*                                                                            *
*  Pop the data off the stack.                                               *
*                                                                            *
*****************************************************************************/

return mac_list_rem_next(stack, NULL, data);

}
